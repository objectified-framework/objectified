import {initCap, toCamelCase} from './util';

const VERSION = '0.1.0';
const CONTROLLER_DIRECTORY = 'src/generated/controller';
const HEADER = '/**\n * This file is automatically generated.\n * Do not modify this file, any changes will be overwritten.\n */\n\n';

(async () => {
  const fs = require('fs');
  const yaml = require('yaml');
  const fileData = fs.readFileSync('./api/openapi.yaml', 'utf8');
  const openapi = yaml.parse(fileData);

  console.log(`Controller Generator: ${VERSION}`);

  fs.rmSync(CONTROLLER_DIRECTORY, { recursive: true, force: true });
  fs.mkdirSync(CONTROLLER_DIRECTORY, { recursive: true });

  console.log('Generating Controllers');

  const tags = {};
  const paths = openapi['paths'];

  for(const tag of openapi['tags']) {
    tags[tag.name] = tag;
    tags[tag.name]['body'] = '';
    tags[tag.name]['refs'] = [];
  }

  for(const path of Object.keys(paths)) {
    for(const [pathMethod, pathMethodData] of Object.entries(paths[path])) {
      const pathTags = pathMethodData['tags'];
      const operationId = pathMethodData['operationId'];
      const requestBody = pathMethodData['requestBody'];
      const responses = pathMethodData['responses'];

      for(const pathTag of pathTags) {
        const description = pathMethodData['description'] ?? `${pathTag}: ${operationId}`;

        tags[pathTag]['body'] += `  /**\n   * ${description.replaceAll('\n', '\n   * ')}\n   */\n`;
        tags[pathTag]['body'] += `  @${initCap(pathMethod)}('${path}')\n`;

        for(const [responseStatus, responseData] of Object.entries(responses)) {
          tags[pathTag]['body'] += `  @ApiResponse({ status: ${responseStatus}, description: '${responseData['description']}' })\n`;
        }

        tags[pathTag]['body'] += `  async ${operationId}(): Promise<any> {\n`;
        tags[pathTag]['body'] += '    return null;\n';
        tags[pathTag]['body'] += '  }\n\n';
      }
    }
  }

  for(const tag of Object.keys(tags)) {
    const outFilename = `${CONTROLLER_DIRECTORY}/${tag}.controller.ts`;
    const tagObject = tags[tag];
    let classBody = '';

    classBody += HEADER;
    classBody += 'import { Post, Get, Delete, Options, Header, Put, Controller } from \'@nestjs/common\';\n';
    classBody += 'import { ApiResponse } from \'@nestjs/swagger\';\n';

    if (tagObject['refs'].length > 0) {
      classBody += `import { ${tagObject['refs'].join(', ')} } from '../dto';\n`;
    }

    classBody += '\n';
    classBody += `/**\n * ${(tagObject['description'] ?? tag).replaceAll('\n', '\n * ')}\n */\n`;
    classBody += `@Controller('${toCamelCase(tag)}')\n`;
    classBody += `export class ${tag}Controller {\n`;
    classBody += tagObject['body'];
    classBody += '}\n';

    fs.writeFileSync(outFilename, classBody, 'utf8');
    console.log(`  Wrote ${outFilename}`);
  }
})();